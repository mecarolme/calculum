#' Function to Sum
#'
#' This function is going to sum
#'
#' @param a A number.
#' @param b It's another number.
#'
#' @examples
#' sum (2, 3)
#'
#' on the console returns [5]

#' @export
sum <- function(a, b) {
  a + b
}

#' Function to Subtract
#'
#' This function is going to subtract
#'
#' @param a A number.
#' @param b It's another number.
#'
#' @examples
#' subtract (2, 3)
#'
#' on the console returns [-1]

#' @export
subtract <- function(a, b) {
  a - b
}
